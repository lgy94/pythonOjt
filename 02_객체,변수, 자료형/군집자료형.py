#사전 : 키(key)와 값(vlaue)의 쌍으로 구성되는 집합의 일종
# ex) 1반 : 35명, 2반 : 32명, 3반 : 30명, 4반 : 33명
count = {1:35, 2:32, 3:30, 4:33}
print (count)
print (type(count))
print (id(count))
